double    Sprint::Game::Circuit::distanceCheckPoint(std::vector<irr::core::vector3df> &s, int index, Sprint::Game::Car &car)
{
    double dist;

    dist = std::sqrt((s.at(index).X - car.getNode().getPosition().X) *
                     (s.at(index).X - car.getNode().getPosition().X) +
                     (s.at(index).Z - car.getNode().getPosition().Z) *
                     (s.at(index).Z - car.getNode().getPosition().Z));

    return (dist);
}

void    Sprint::Game::Circuit::checkSpeedBoost(Sprint::Game::Car &car)
{
    double dist;

    for (auto const &boost : this->speed_boost)
    {
        dist = std::sqrt((boost.X - car.getNode().getPosition().X) *
                         (boost.X - car.getNode().getPosition().X) +
                         (boost.Y - car.getNode().getPosition().Y) *
                         (boost.Y - car.getNode().getPosition().Y) +
                         (boost.Z - car.getNode().getPosition().Z) *
                         (boost.Z - car.getNode().getPosition().Z));

        if (dist < 1)
            car.setSpeed(car.getMaxSpeed() * 1.5);
    }
}

void    Sprint::Game::Circuit::checkSlowBoost(Sprint::Game::Car &car)
{
    double dist;

    for (auto const &boost : this->slow_boost)
    {
        dist = std::sqrt((boost.X - car.getNode().getPosition().X) *
                         (boost.X - car.getNode().getPosition().X) +
                         (boost.Y - car.getNode().getPosition().Y) *
                         (boost.Y - car.getNode().getPosition().Y) +
                         (boost.Z - car.getNode().getPosition().Z) *
                         (boost.Z - car.getNode().getPosition().Z));

        if (dist < 4.6 && car.getSpeed() > 60)
            car.setSpeed(car.getSpeed() - 3);
    }
}

void    Sprint::Game::Circuit::checkGravityBoost(Sprint::Game::Model m, Sprint::Game::Car &car, Sprint::Physic &physic)
{
    double dist;

    for (auto const &boost : this->gravity_boost)
    {
        dist = std::sqrt((boost.X - car.getNode().getPosition().X) *
                         (boost.X - car.getNode().getPosition().X) +
                         (boost.Y - car.getNode().getPosition().Y) *
                         (boost.Y - car.getNode().getPosition().Y) +
                         (boost.Z - car.getNode().getPosition().Z) *
                         (boost.Z - car.getNode().getPosition().Z));

        if (dist < 1)
            physic.setCarGravity(m, irr::core::vector3df(0, -0.02, 0));
        if (dist > 10 && dist < 11)
            physic.setCarGravity(m, irr::core::vector3df(0, -0.4, 0));
    }
}

