static int	show_type(const size_t param, const int nb_param)
{
  if (nb_param > 0)
    printf(", ");
  return (printf(HEX_PARAM, param));
}

void		display_syscall(t_ftrace *ftrace,
				const long syscall,
				const struct user_regs_struct *regs,
				const size_t ret)
{
  int		i;

  i = -1;
  if (syscall >= 0 && syscall <= 331)
    {
      printf(SYSCALL, g_syscalls[syscall].name);
      add_to_graph(ftrace->fd, ftrace->func, g_syscalls[syscall].name, SYS);
      while (++i < g_syscalls[syscall].nb_param)
	show_type(get_param(regs, i), i);
      printf(HEX_RET, ret);
    }
}
